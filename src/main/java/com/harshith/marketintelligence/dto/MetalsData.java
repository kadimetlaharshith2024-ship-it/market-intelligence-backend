package com.harshith.marketintelligence.dto;

public class MetalsData {

    private double gold;
    private double silver;
    private double copper;
    private double aluminum;
    private double platinum;

    public double getGold() {
        return gold;
    }

    public void setGold(double gold) {
        this.gold = gold;
    }

    public double getSilver() {
        return silver;
    }

    public void setSilver(double silver) {
        this.silver = silver;
    }

    public double getCopper() {
        return copper;
    }

    public void setCopper(double copper) {
        this.copper = copper;
    }

    public double getAluminum() {
        return aluminum;
    }

    public void setAluminum(double aluminum) {
        this.aluminum = aluminum;
    }

    public double getPlatinum() {
        return platinum;
    }

    public void setPlatinum(double platinum) {
        this.platinum = platinum;
    }
}